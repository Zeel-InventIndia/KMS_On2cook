/**
 * Test if a Google Drive URL is accessible
 * Returns a promise that resolves with success/failure info
 */
export const testGoogleDriveUrl = async (url: string, timeoutMs = 5000): Promise<{
  success: boolean;
  url: string;
  error?: string;
  status?: number;
}> => {
  if (!url) {
    return { success: false, url, error: 'Empty URL' };
  }

  try {
    const controller = new AbortController();
    const timeoutId = setTimeout(() => controller.abort(), timeoutMs);
    
    const response = await fetch(url, {
      method: 'HEAD', // Use HEAD to avoid downloading the full image
      signal: controller.signal,
      mode: 'cors',
      credentials: 'omit',
      cache: 'no-cache'
    });
    
    clearTimeout(timeoutId);
    
    return {
      success: response.ok,
      url,
      status: response.status,
      error: response.ok ? undefined : `HTTP ${response.status} ${response.statusText}`
    };
  } catch (error) {
    if (error instanceof Error) {
      return {
        success: false,
        url,
        error: error.name === 'AbortError' ? 'Timeout' : error.message
      };
    }
    return {
      success: false,
      url,
      error: 'Unknown error'
    };
  }
};

/**
 * Test multiple Google Drive URL variants and return the first working one
 */
export const findWorkingGoogleDriveUrl = async (url: string): Promise<string | null> => {
  const variants = getGoogleDriveUrlVariants(url);
  
  console.log(`🔍 Testing ${variants.length} URL variants for accessibility...`);
  
  // Test URLs in parallel for better performance
  const testPromises = variants.slice(0, 5).map(async (variantUrl, index) => {
    const result = await testGoogleDriveUrl(variantUrl, 3000); // 3 second timeout per URL
    return { ...result, index, originalUrl: variantUrl };
  });
  
  try {
    // Wait for the first successful result
    const results = await Promise.allSettled(testPromises);
    
    for (const result of results) {
      if (result.status === 'fulfilled' && result.value.success) {
        console.log(`✅ Found working URL (variant ${result.value.index + 1}):`, result.value.url);
        return result.value.url;
      }
    }
    
    // Log all failures for debugging
    console.warn('❌ All URL variants failed:', results.map(r => 
      r.status === 'fulfilled' ? `${r.value.url}: ${r.value.error}` : 'Promise rejected'
    ));
    
    return null;
  } catch (error) {
    console.error('💥 Error testing URL variants:', error);
    return null;
  }
};

/**
 * Enhanced URL variant generator with intelligent ordering based on current success patterns
 */
export const getSmartGoogleDriveUrlVariants = (url: string): string[] => {
  const baseVariants = getGoogleDriveUrlVariants(url);
  
  // Reorder based on observed success patterns
  // Put the most reliable formats first based on current behavior
  const reliabilityOrder = [
    'lh3.googleusercontent.com/d/', // Usually most reliable
    'drive.google.com/uc?export=view', // Good for public files
    'drive.google.com/uc?id=', // Alternative export format
    'drive.google.com/thumbnail?id=', // May have access restrictions
  ];
  
  const orderedVariants: string[] = [];
  const remainingVariants = [...baseVariants];
  
  // Add variants in order of reliability
  for (const pattern of reliabilityOrder) {
    const matchingVariants = remainingVariants.filter(v => v.includes(pattern));
    orderedVariants.push(...matchingVariants);
    // Remove already added variants
    matchingVariants.forEach(v => {
      const index = remainingVariants.indexOf(v);
      if (index > -1) remainingVariants.splice(index, 1);
    });
  }
  
  // Add any remaining variants
  orderedVariants.push(...remainingVariants);
  
  return Array.from(new Set(orderedVariants)); // Remove duplicates
};