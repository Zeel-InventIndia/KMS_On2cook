/**
 * Get recipe image URL by recipe name
 * Supports fuzzy matching for variations in recipe names
 */
export const getRecipeImageUrl = (recipeName: string): string | null => {
  if (!recipeName) return null;
  
  // Normalize the recipe name for comparison
  const normalizedInput = recipeName.trim().toUpperCase();
  
  // Direct match first
  if (RECIPE_IMAGE_MAPPING[normalizedInput]) {
    console.log(`🍽️ Direct match found for recipe: ${recipeName}`);
    return RECIPE_IMAGE_MAPPING[normalizedInput];
  }
  
  // Fuzzy matching - find the best match
  const recipeKeys = Object.keys(RECIPE_IMAGE_MAPPING);
  
  // Try partial matches (recipe name contains the key or vice versa)
  for (const key of recipeKeys) {
    if (normalizedInput.includes(key) || key.includes(normalizedInput)) {
      console.log(`🍽️ Partial match found for recipe: ${recipeName} -> ${key}`);
      return RECIPE_IMAGE_MAPPING[key];
    }
  }
  
  // Try word-based matching for compound names
  const inputWords = normalizedInput.split(/\s+/);
  for (const key of recipeKeys) {
    const keyWords = key.split(/\s+/);
    const commonWords = inputWords.filter(word => keyWords.includes(word));
    
    // If we have significant word overlap, consider it a match
    if (commonWords.length >= Math.min(2, Math.min(inputWords.length, keyWords.length))) {
      console.log(`🍽️ Word-based match found for recipe: ${recipeName} -> ${key}`);
      return RECIPE_IMAGE_MAPPING[key];
    }
  }
  
  console.log(`🍽️ No matching image found for recipe: ${recipeName}`);
  return null;
};

/**
 * Convert Google Drive sharing URL to direct image URL with multiple fallback formats
 */
export const convertGoogleDriveUrl = (url: string): string => {
  if (!url || !url.includes('drive.google.com')) {
    return url;
  }
  
  console.log(`🔄 Converting Google Drive URL: ${url}`);
  
  // Enhanced patterns to handle various Google Drive URL formats
  const patterns = [
    /\/file\/d\/([a-zA-Z0-9-_]+)\/view/, // View format with /view suffix (most common for shares)
    /\/file\/d\/([a-zA-Z0-9-_]+)/, // Standard sharing URL
    /\/d\/([a-zA-Z0-9-_]+)/, // Short format
    /id=([a-zA-Z0-9-_]+)/, // Query parameter format
    /\/open\?id=([a-zA-Z0-9-_]+)/ // Open format
  ];
  
  for (let i = 0; i < patterns.length; i++) {
    const pattern = patterns[i];
    const match = url.match(pattern);
    if (match) {
      const fileId = match[1];
      // Use the thumbnail format which is more reliable for images
      const converted = `https://drive.google.com/thumbnail?id=${fileId}&sz=w400`;
      console.log(`🔄 Converted using pattern ${i + 1}: ${converted}`);
      return converted;
    }
  }
  
  console.warn(`⚠️ Could not convert Google Drive URL: ${url}`);
  return url;
};

/**
 * Get multiple Google Drive URL formats for fallback attempts
 * Enhanced with more reliable URL formats and better error handling
 */
export const getGoogleDriveUrlVariants = (url: string): string[] => {
  if (!url) {
    return [];
  }
  
  // If it's not a Google Drive URL, return as-is
  if (!url.includes('drive.google.com')) {
    return [url];
  }
  
  const patterns = [
    /\/file\/d\/([a-zA-Z0-9-_]+)\/view/, // View format with /view suffix
    /\/file\/d\/([a-zA-Z0-9-_]+)/, // Standard sharing URL
    /\/d\/([a-zA-Z0-9-_]+)/, // Short format
    /id=([a-zA-Z0-9-_]+)/, // Query parameter format
    /\/open\?id=([a-zA-Z0-9-_]+)/ // Open format
  ];
  
  let fileId = null;
  for (const pattern of patterns) {
    const match = url.match(pattern);
    if (match && match[1]) {
      fileId = match[1];
      break;
    }
  }
  
  if (!fileId || fileId.length < 10) {
    console.warn(`⚠️ Could not extract valid file ID from Google Drive URL: ${url}`);
    return [url]; // Return original URL if extraction fails
  }
  
  // Return multiple URL variants to try, ordered by most reliable first
  // Based on current Google Drive API behavior and access patterns
  const variants = [
    // Most reliable formats (public access)
    `https://lh3.googleusercontent.com/d/${fileId}=w400-h400-c`, // Google User Content with crop
    `https://drive.google.com/uc?export=view&id=${fileId}`, // Universal export format
    `https://lh3.googleusercontent.com/d/${fileId}=w400`, // Google User Content with size
    
    // Medium reliability (may require specific permissions)
    `https://drive.google.com/thumbnail?id=${fileId}&sz=w400`, // Standard thumbnail
    `https://drive.google.com/thumbnail?id=${fileId}&sz=w300`, // Smaller thumbnail
    `https://drive.google.com/thumbnail?id=${fileId}&sz=s220`, // Small size format
    
    // Alternative formats
    `https://lh3.googleusercontent.com/d/${fileId}`, // Google User Content without size
    `https://drive.google.com/file/d/${fileId}/preview`, // Preview format
    
    // Backup formats with different parameters
    `https://drive.google.com/uc?id=${fileId}&export=download`, // Download as view
    `https://drive.google.com/uc?authuser=0&id=${fileId}&export=view`, // With auth user
    
    // Last resort - original URL
    url
  ];
  
  // Remove duplicates while preserving order
  return Array.from(new Set(variants));
};

